 Dname VARCHAR(15) NOT NULL,
 Dnumber INT PRIMARY KEY,
 Mgr_ssn VARCHAR(9) NOT NULL,
 Mgr_start_date DATE,
 FOREIGN KEY (Mgr_ssn) REFERENCES EMPLOYEE15(Ssn)
 );
 
